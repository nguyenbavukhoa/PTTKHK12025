    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

        try (Connection con = JDBCUtil.getConnection()) {
            con.setAutoCommit(false); // Bắt đầu transaction

            // 1. Kiểm tra các trường đầu vào
            if (idTF.getText().isEmpty() || productNameTF.getText().isEmpty() || sellPriceTF.getText().isEmpty()
                    || importPriceTF.getText().isEmpty() || originTF.getText().isEmpty()
                    || supplierTF.getText().isEmpty() || quantityTF.getText().isEmpty()) {

                JOptionPane.showMessageDialog(null, "Please insert all required data.");
                Function.checkTextField(idTF);
                Function.checkTextField(productNameTF);
                Function.checkTextField(sellPriceTF);
                Function.checkTextField(importPriceTF);
                Function.checkTextField(originTF);
                Function.checkTextField(supplierTF);
                Function.checkTextField(quantityTF);
                return;
            }

            // 2. Kiểm tra xem nhà cung cấp đã tồn tại chưa
            String supplierName = supplierTF.getText();
            String maNhaCungCap = null;

            String checkSupplierQuery = "SELECT ma_nha_cung_cap FROM nha_cung_cap WHERE ten_nha_cung_cap = ?";
            try (PreparedStatement checkSupplierStmt = con.prepareStatement(checkSupplierQuery)) {
                checkSupplierStmt.setString(1, supplierName);
                ResultSet rs = checkSupplierStmt.executeQuery();

                if (rs.next()) {
                    // Nhà cung cấp đã tồn tại
                    maNhaCungCap = rs.getString("ma_nha_cung_cap");
                } else {
                    // 3. Nếu nhà cung cấp chưa tồn tại, tạo mã và thêm mới
                    maNhaCungCap = "NCC" + Function.generateUniqueRandomString(con, "nha_cung_cap", "ma_nha_cung_cap", 6);

                    String insertSupplierQuery = "INSERT INTO nha_cung_cap (ma_nha_cung_cap, ten_nha_cung_cap) VALUES (?, ?)";
                    try (PreparedStatement insertSupplierStmt = con.prepareStatement(insertSupplierQuery)) {
                        insertSupplierStmt.setString(1, maNhaCungCap);
                        insertSupplierStmt.setString(2, supplierName);
                        insertSupplierStmt.executeUpdate();
                    }
                }
            }

            // 4. Thêm sản phẩm mới
            String insertProductQuery = "INSERT INTO PRODUCTS (ID, PRODUCT_NAME, SELL_PRICE, ORIGIN, SUPPLIER, PRODUCT_TYPE, QUANTITY) VALUES (?, ?, ?, ?, ?, ?, ?)";
            try (PreparedStatement insertProductStmt = con.prepareStatement(insertProductQuery)) {
                insertProductStmt.setString(1, idTF.getText());
                insertProductStmt.setString(2, productNameTF.getText());
                insertProductStmt.setString(3, sellPriceTF.getText());
                insertProductStmt.setString(4, originTF.getText());
                insertProductStmt.setString(5, maNhaCungCap); // Gắn mã nhà cung cấp mới hoặc hiện tại
                insertProductStmt.setString(6, (String) productTypeCBB.getSelectedItem());
                insertProductStmt.setString(7, quantityTF.getText());
                insertProductStmt.executeUpdate();
            }

            // 5. Tạo phiếu nhập và chi tiết phiếu nhập nếu số lượng > 1
            int quantity = Integer.parseInt(quantityTF.getText());
            if (quantity > 1) {
                String maPhieuNhap = "PN" + Function.generateUniqueRandomString(con, "PHIEUNHAP", "ma_phieu_nhap", 8);
                String today = java.time.LocalDate.now().toString();

                String insertPhieuNhapQuery = "INSERT INTO PHIEUNHAP (ma_phieu_nhap, thoi_gian, trang_thai, ma_nha_cung_cap, account_id) VALUES (?, ?, ?, ?, ?)";
                String insertCtPhieuNhapQuery = "INSERT INTO CTPHIEUNHAP (ma_phieu_nhap, ma_san_pham, quantity, don_gia) VALUES (?, ?, ?, ?)";

                try (PreparedStatement prPhieuNhap = con.prepareStatement(insertPhieuNhapQuery); PreparedStatement prCtPhieuNhap = con.prepareStatement(insertCtPhieuNhapQuery)) {

                    // Thêm phiếu nhập
                    prPhieuNhap.setString(1, maPhieuNhap);
                    prPhieuNhap.setString(2, today); // Ngày nhập là ngày hiện tại
                    prPhieuNhap.setInt(3, 1); // Trạng thái là 1 (đã nhập)
                    prPhieuNhap.setString(4, maNhaCungCap); // Mã nhà cung cấp
                    prPhieuNhap.setString(5, "admin"); // ID tài khoản (hardcode hoặc lấy từ session)
                    prPhieuNhap.executeUpdate();

                    // Thêm chi tiết phiếu nhập
                    prCtPhieuNhap.setString(1, maPhieuNhap);
                    prCtPhieuNhap.setString(2, idTF.getText()); // Mã sản phẩm
                    prCtPhieuNhap.setInt(3, quantity); // Số lượng nhập
                    prCtPhieuNhap.setInt(4, Integer.parseInt(importPriceTF.getText())); // Đơn giá nhập
                    prCtPhieuNhap.executeUpdate();
                }
            }

            con.commit(); // Xác nhận transaction
            JOptionPane.showMessageDialog(null, "Product and Supplier added successfully!");

            this.dispose();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "SQL Error: " + e.getMessage());
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }


    }//GEN-LAST:event_addButtonActionPerformed

