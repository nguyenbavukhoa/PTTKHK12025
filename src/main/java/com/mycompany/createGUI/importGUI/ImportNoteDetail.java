    private void confirmNoteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmNoteBtnActionPerformed
        try (Connection con = JDBCUtil.getConnection(); PreparedStatement confirmNote = con.prepareStatement("UPDATE phieunhap SET trang_thai = 1 WHERE ma_phieu_nhap = ?"); PreparedStatement updateProductQuantity = con.prepareStatement(
                "UPDATE products p "
                + "SET p.quantity = p.quantity + ("
                + "    SELECT SUM(ct.quantity) "
                + "    FROM ctphieunhap ct "
                + "    WHERE ct.ma_phieu_nhap = ? AND ct.ma_san_pham = p.id"
                + ") "
                + "WHERE EXISTS ("
                + "    SELECT 1 "
                + "    FROM ctphieunhap ct "
                + "    WHERE ct.ma_phieu_nhap = ? AND ct.ma_san_pham = p.id"
                + ")"
        )) {

            // Gán giá trị cho tham số để xác nhận phiếu nhập
            confirmNote.setString(1, idPhieuNhap);

            // Thực thi câu lệnh xác nhận phiếu nhập
            int rowsUpdated = confirmNote.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Xác nhận phiếu nhập thành công!");

                // Gán giá trị cho tham số để cập nhật số lượng sản phẩm
                updateProductQuantity.setString(1, idPhieuNhap);
                updateProductQuantity.setString(2, idPhieuNhap);

                // Thực thi câu lệnh cập nhật quantity
                int productRowsUpdated = updateProductQuantity.executeUpdate();
                if (productRowsUpdated > 0) {
                    System.out.println("Cập nhật số lượng sản phẩm thành công! Số dòng bị ảnh hưởng: " + productRowsUpdated);
                } else {
                    System.out.println("Không có sản phẩm nào để cập nhật số lượng.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy phiếu nhập cần cập nhật trong cơ sở dữ liệu.");
            }

            // Đóng giao diện hiện tại sau khi xác nhận thành công
            this.dispose();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi: " + ex.getMessage());
        }

    }//GEN-LAST:event_confirmNoteBtnActionPerformed

