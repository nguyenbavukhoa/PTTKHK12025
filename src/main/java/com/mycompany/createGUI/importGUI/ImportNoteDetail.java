    public ImportNoteDetail(String id, String trangThai) {
        initComponents();
        idPhieuNhap = id;

        if (!trangThai.equals("0")) {
            confirmNoteBtn.setEnabled(false);
            killNoteBtn.setEnabled(false);
        } else {
            confirmNoteBtn.setEnabled(true);
            killNoteBtn.setEnabled(true);
        }

        this.choiseProductTableModel = (DefaultTableModel) choiseProductTable.getModel();
        choiseProductTableModel.setRowCount(0);
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        String query = "SELECT * "
                + " FROM phieunhap p"
                + " Join ctphieunhap ctp "
                + " On p.ma_phieu_nhap = ctp.ma_phieu_nhap "
                + " Join products pr "
                + " On pr.id = ctp.ma_san_pham "
                + " Join nha_cung_cap ncp"
                + " On p.ma_nha_cung_cap = nha_cung_cap.ma_nha_cung_cap "
                + " Where ctp.ma_phieu_nhap = ?";

        try (Connection con = JDBCUtil.getConnection(); ResultSet resultSet = JDBCUtil.getResultSet(con, query, 1, id)) {
            if (resultSet.next()) {

                // Gán giá trị và tắt thay đổi
                idPhieuNhapTF.setText(resultSet.getString("p.ma_phieu_nhap"));
                idPhieuNhapTF.setEnabled(false);

                accountNameTF.setText(resultSet.getString("p.account_id"));
                accountNameTF.setEnabled(false);

                supplierNameTF.setText(resultSet.getString("ncp.ten_nha_cung_cap"));
                supplierNameTF.setEnabled(false);

                dateTF.setText(resultSet.getDate("p.thoi_gian").toString());
                dateTF.setEnabled(false);

                choiseProductTableModel.setRowCount(0);
                do {
                    // Get values from the selected row
                    int nextSTT = choiseProductTableModel.getRowCount() + 1; // STT tiếp theo
                    String productId = resultSet.getString("ctp.ma_san_pham");
                    String name = resultSet.getString("pr.product_name");
                    String importPrice = Function.dinhDangTien(resultSet.getInt("ctp.don_gia"));
                    String quantity = resultSet.getString("ctp.quantity");

                    Object[] row = {nextSTT, id, name, importPrice, quantity};
                    choiseProductTableModel.addRow(row);
                } while (resultSet.next());
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy phiếu !");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ImportNoteDetail.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

